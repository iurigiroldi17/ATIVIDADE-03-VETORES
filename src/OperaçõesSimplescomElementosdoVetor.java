public class OperaçõesSimplescomElementosdoVetor {
    public static void main(String[] args) {

        int[] vetor = {3, 6, 5, 12};

        int primeiroElemento = vetor[0];
        int ultimoElemento = vetor[1];

        int soma = primeiroElemento + ultimoElemento;

        System.out.println("A soma do primeiro e do ultimo elemento: " + soma);
    }
}
